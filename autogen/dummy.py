from math import ceil


def add(a: int, b: int) -> int:
    return a + b


def ag2_ceil(x: float) -> int:
    return ceil(x)


def sub(a: int, b: int) -> int:
    return a - b


def mul(a: int, b: int) -> int:
    return a * b


def div(a: int, b: int) -> int:
    return a / b
